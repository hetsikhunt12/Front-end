#include <iostream>
using namespace std;

int main() {
    // Variables
    int a = 10;
    float b = 5.5;
    char c = 'A';

    // Constant
    const int x = 20;

    cout << "Variable int a = " << a << endl;
    cout << "Variable float b = " << b << endl;
    cout << "Variable char c = " << c << endl;

    cout << "Constant x = " << x << endl;

    // Operations
    cout << "a + x = " << a + x << endl;
    cout << "b * a = " << b * a << endl;

    return 0;
}
